package com.sujata.demo;

public class DemoOne {

	public static void main(String[] args) {
		int value;
		
		value=10;
		System.out.println(value);
		
		value=0x10;
		System.out.println(value);
		
		value=010;
		System.out.println(value);
		
		value=0b10;
		System.out.println(value);

	}

}
